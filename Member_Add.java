        private void table_update()
    {
        try {
            int c;
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/ssoc","root","");
                
                pst = con.prepareStatement("select * from member");
                ResultSet rs = pst.executeQuery();
                
                ResultSetMetaData rsd = rs.getMetaData();
                c= rsd.getColumnCount();
                
                DefaultTableModel d = (DefaultTableModel)jTable1.getModel();            
                d.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1; i<=c; i++)
                    {
                        v2.add(rs.getString("id"));
                        v2.add(rs.getString("fname"));
                        v2.add(rs.getString("iname"));
                         v2.add(rs.getString("nic"));
                          v2.add(rs.getString("gender"));
                           v2.add(rs.getString("address"));
                            v2.add(rs.getString("mobile"));
                    }
                    
                    d.addRow(v2);
                }
                 
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Member_Add.class.getName()).log(Level.SEVERE, null, ex);
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(Member_Add.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        
        
    }
    
